for lang, trans in translations.items():
    # Format the translation strings
    formatted_trans = ""
    for k, v in trans.items():
        formatted_trans += f'      "{k}": "{v}",\n'
    
    # We look for a pattern like:
    # lang: {
    #   translation: {
    # OR
    # 'lang': {
    #   translation: {
    
    # Regex to find the translation block for the specific language
    pattern = r'(' + lang + r':\s*\{\s*translation:\s*\{)'
    
    def repl(match):
        return match.group(1) + '\n' + formatted_trans
        
    content = re.sub(pattern, repl, content, count=1)

